                   My stay was at a small town called <b><i>"Neumunster"</i></b> in the northern germany. It is a 100 miles from the city of Hamburg.
                   A small town, but it is well connected by the rail networks and in fact it is a vital hub for the northern part of the rail network in germany.
                   The city has dedicated regions for the industrial developments in it's north and south regions. With small bus networks and a group of taxies
                   the transportation inside the city is moderate and it has a host of 3 star hotels and furnished apartments for lodging. German food groups
                   dominates the restaurents. Though indian and chineese cusines are available they are mostly adapted for germany food habits.
                   The main part of the city is a aprroximately 1 km long road surrounded by an array of brick and tile buildings.
                   Shopping malls and returants are the main tenants in this region.
                   A water canal and a surrounding park marks the center of the city and provides a relaxing environment.
                   Apart from these the city contains inside it a small wild park in the name of TierPark, holding small number of wild animal attractions.

