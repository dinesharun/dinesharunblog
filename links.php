function GetPosts($catID, $idxID)
{
  global $links;
  $i = 0;
  $posts = array();
  
  foreach($links as $post)
  {
    if((($post["catId"] == $catID) && ($post["idxId"] == $idxID)) || ($catID == 0) || ($idxID == 0))
    {
      $posts[$i] = $post;
      $i++;
    }
  }
  
  return $posts;
}

function GetPost($postID)
{
  global $links;
  global $numPosts;
  
  if(($postID != 0) && ($postID <= $numPosts))
  {
    return $links[$postID];
  }
  else
  {
    return $links[0];
  }
}

